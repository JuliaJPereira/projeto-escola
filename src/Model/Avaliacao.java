package Model;

public class Avaliacao {
	Integer codAluno;
	Integer codDisciplina;
	Integer numAvaliacao;
	Double valorNota;
	
	public Integer getCodAluno() {
		return codAluno;
	}
	public void setCodAluno(Integer codAluno) {
		this.codAluno = codAluno;
	}
	public Integer getCodDisciplina() {
		return codDisciplina;
	}
	public void setCodDisciplina(Integer codDisciplina) {
		this.codDisciplina = codDisciplina;
	}
	public Integer getNumAvaliacao() {
		return numAvaliacao;
	}
	public void setNumAvaliacao(Integer numAvaliacao) {
		this.numAvaliacao = numAvaliacao;
	}
	public Double getValorNota() {
		return valorNota;
	}
	public void setValorNota(Double valorNota) {
		this.valorNota = valorNota;
	}
	
	
}
